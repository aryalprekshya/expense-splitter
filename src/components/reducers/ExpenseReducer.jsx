const ExpenseReducer = (state, action) => {
  switch (action.type) {
    case "ADD_EXPENSE":
      return null;

    case "EDIT_EXPENSE":
      return null;

    case "DELETE_EXPENSE":
      return null;

    case "CALCULATE_TOTAL":
      return null;

    default:
      return state;
  }
};

export default ExpenseReducer;
